export async function getStaticPaths({ locales }) {
  const { data } = await hygraph.query({
    query: gql`
      query CATEGORIES {
        categories {
          id
          slug
        }
      }
    `,
  });

  const paths = data.categories.flatMap(category => {
    return locales.map(locale => ({
      locale,
      params: {
        categorySlug: category.slug,
      },
    }))
  });

  return {
    paths,
    fallback: false,
  }
}

export async function getStaticProps({ params, locale }) {
  await avoidTooManyRequestsError();

  const { data } = await hygraph.query({
    query: gql`
      query CATEGORY($categorySlug: String, $locale: Locale!) {
        category(where: { slug: $categorySlug }, locales: [$locale]) {
          id
          name
          slug
          products {
            id
            name
            slug
            price
            image
          }
        }
      }
    `,
    variables: {
      categorySlug: params.categorySlug,
      locale: locale.replace('-', '_'),
    }
  });

  return {
    props: {
      category: data.category,
      products: data.category.products,
    }
  }
}
